func TestEmptyFile(t *testing.T) {
	nf := &NinjaFile{}
	var b strings.Builder
	_, err := nf.WriteTo(&b)
	if err != nil {
		t.Fatalf("WriteTo failed: %v", err)
	}
	if b.String() != "" {
		t.Errorf("empty file should produce empty output, got %q", b.String())
	}
}

func TestComments(t *testing.T) {
	nf := &NinjaFile{}
	nf.AddComment("Generated by bootstrap")
	nf.AddComment("")
	nf.AddComment("Stage 1: build ninja_mod")

	got := mustRender(t, nf)
	// Empty comment produces a bare newline (visual separator)
	if !strings.HasPrefix(got, "# Generated by bootstrap\n") {
		t.Errorf("first comment missing, got:\n%s", got)
	}
	if !strings.Contains(got, "# Stage 1: build ninja_mod") {
		t.Errorf("second comment missing, got:\n%s", got)
	}
}

func TestVariables(t *testing.T) {
	nf := &NinjaFile{}
	nf.AddVariable("cxx", "g++")
	nf.AddVariable("cflags", "-O2 -Wall")

	got := mustRender(t, nf)
	if !strings.Contains(got, "cxx = g++\n") {
		t.Errorf("missing cxx variable, got:\n%s", got)
	}
	if !strings.Contains(got, "cflags = -O2 -Wall\n") {
		t.Errorf("missing cflags variable, got:\n%s", got)
	}
}

func TestPool(t *testing.T) {
	nf := &NinjaFile{}
	nf.AddPool("link_pool", 4)

	got := mustRender(t, nf)
	want := "pool link_pool\n  depth = 4\n"
	if !strings.Contains(got, want) {
		t.Errorf("pool mismatch, got:\n%s", got)
	}
}

func TestRuleMinimal(t *testing.T) {
	nf := &NinjaFile{}
	nf.AddRule(Rule{
		Name:    "cxx",
		Command: "$cxx $cflags -c $in -o $out",
	})

	got := mustRender(t, nf)
	want := "rule cxx\n  command = $cxx $cflags -c $in -o $out\n\n"
	if got != want {
		t.Errorf("rule mismatch:\ngot:\n%s\nwant:\n%s", got, want)
	}
}

func TestRuleFull(t *testing.T) {
	nf := &NinjaFile{}
	nf.AddRule(Rule{
		Name:        "cxx",
		Command:     "$cxx -MMD -MT $out -MF $out.d $cflags -c $in -o $out",
		Description: "CXX $out",
		Depfile:     "$out.d",
		Deps:        "gcc",
		Restat:      true,
		Variables: []Variable{
			{"extra", "-Werror"},
		},
	})

	got := mustRender(t, nf)
	lines := strings.Split(got, "\n")

	assertLine(t, lines, "rule cxx")
	assertLine(t, lines, "  command = $cxx -MMD -MT $out -MF $out.d $cflags -c $in -o $out")
	assertLine(t, lines, "  description = CXX $out")
	assertLine(t, lines, "  depfile = $out.d")
	assertLine(t, lines, "  deps = gcc")
	assertLine(t, lines, "  restat = true")
	assertLine(t, lines, "  extra = -Werror")
}

func TestBuildBasic(t *testing.T) {
	nf := &NinjaFile{}
	nf.AddBuild(Build{
		RuleName: "cxx",
		Outputs:  []string{"build/foo.o"},
		Inputs:   []string{"src/foo.cc"},
	})

	got := mustRender(t, nf)
	want := "build build/foo.o: cxx src/foo.cc\n"
	if !strings.Contains(got, want) {
		t.Errorf("build edge mismatch, got:\n%s", got)
	}
}

func TestBuildWithImplicitsAndOrderOnly(t *testing.T) {
	nf := &NinjaFile{}
	nf.AddBuild(Build{
		RuleName:  "link",
		Outputs:   []string{"build/ninja_mod"},
		Inputs:    []string{"build/ninja.o"},
		Implicits: []string{"build/libninja.a"},
		OrderOnly: []string{"build/stamp"},
	})

	got := mustRender(t, nf)
	want := "build build/ninja_mod: link build/ninja.o | build/libninja.a || build/stamp\n"
	if !strings.Contains(got, want) {
		t.Errorf("build edge with deps mismatch, got:\n%s", got)
	}
}

