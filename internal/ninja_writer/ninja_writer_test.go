func TestFullNinjaFile(t *testing.T) {
	nf := &NinjaFile{}
	nf.AddComment("Stage 1: build ninja_mod from source")
	nf.AddVariable("cxx", "g++")
	nf.AddVariable("cflags", "-O2 -Wall -Wextra -Wno-deprecated -fno-rtti -pthread")

	nf.AddRule(Rule{
		Name:        "cxx",
		Command:     "$cxx -MMD -MT $out -MF $out.d $cflags -c $in -o $out",
		Description: "CXX $out",
		Depfile:     "$out.d",
		Deps:        "gcc",
	})

	nf.AddRule(Rule{
		Name:        "ar",
		Command:     "rm -f $out && ar crs $out $in",
		Description: "AR $out",
	})

	nf.AddRule(Rule{
		Name:        "link",
		Command:     "$cxx -o $out $in $libs",
		Description: "LINK $out",
	})

	nf.AddBuild(Build{
		RuleName: "cxx",
		Outputs:  []string{"$builddir/build.o"},
		Inputs:   []string{"$src/build.cc"},
	})

	nf.AddBuild(Build{
		RuleName: "ar",
		Outputs:  []string{"$builddir/libninja.a"},
		Inputs:   []string{"$builddir/build.o"},
	})

	nf.AddBuild(Build{
		RuleName:  "link",
		Outputs:   []string{"$builddir/ninja_mod"},
		Inputs:    []string{"$builddir/ninja.o"},
		Implicits: []string{"$builddir/libninja.a"},
		Variables: []Variable{
			{"libs", "-L$builddir -lninja -lpthread"},
		},
	})

	got := mustRender(t, nf)

	// Verify structure ordering: comments → variables → rules → builds
	commentIdx := strings.Index(got, "# Stage 1")
	varIdx := strings.Index(got, "cxx = ")
	ruleIdx := strings.Index(got, "rule cxx")
	buildIdx := strings.Index(got, "build $builddir/build.o:")

	if !(commentIdx < varIdx && varIdx < ruleIdx && ruleIdx < buildIdx) {
		t.Errorf("section ordering wrong: comment=%d var=%d rule=%d build=%d",
			commentIdx, varIdx, ruleIdx, buildIdx)
	}
}

func mustRender(t *testing.T, nf *NinjaFile) string {
	t.Helper()
	var b strings.Builder
	_, err := nf.WriteTo(&b)
	if err != nil {
		t.Fatalf("WriteTo failed: %v", err)
	}
	return b.String()
}

func assertLine(t *testing.T, lines []string, want string) {
	t.Helper()
	for _, l := range lines {
		if l == want {
			return
		}
	}
	t.Errorf("line %q not found in output lines: %v", want, lines)
}
